#!/usr/bin/env python3
"""CLI entrypoint for Adlist-Parser.

Runs the pipeline for both the main adlist and the whitelist using
defaults under the repository's ``data/`` and writes results to ``output/``.
"""

from __future__ import annotations

from asyncio import gather, run, sleep, to_thread
from datetime import datetime, timezone
from time import time
from typing import List, Optional, Tuple

from .constants import ADLIST_OUTPUT, ADLISTS, WHITELIST_OUTPUT, WHITELISTS
from .content import generate_list, separate_blocklist_whitelist
from .fetcher import fetch
from .io import load_sources, write_output
from .models import Source
from .redundancy import generate_redundancy_report
from .reporting import generate_report
from .status import GroupedStatusDisplay, StatusSpinner

status_display: Optional[GroupedStatusDisplay] = None


def _generate_header(
    list_type: str,
    total_entries: int,
    domains: int,
    abp_rules: int,
    sources_count: int,
) -> str:
    """Generate header comment block for output files.

    Args:
        list_type: Type of list ('Adlist' or 'Whitelist').
        total_entries: Total number of entries in the list.
        domains: Number of domain entries.
        abp_rules: Number of ABP-style rules.
        sources_count: Number of source lists processed.

    Returns:
        Formatted header comment string.
    """
    now = datetime.now(timezone.utc)
    timestamp = now.strftime("%Y-%m-%d %H:%M:%S UTC")

    header = f"""# {list_type} - Generated by Adlist-Parser
# https://github.com/Toomas633/Adlist-Parser
#
# Created: {timestamp}
# Total entries: {total_entries:,}
# Domains: {domains:,}
# ABP-style rules: {abp_rules:,}
# Sources processed: {sources_count}
#
# This file is automatically generated. Do not edit manually.
# To update, run: adlist-parser or python -m adparser
#
"""
    return header


async def _fetch_with_progress(sources: List[Source], spinner: StatusSpinner):
    """Fetch sources with progress updates to the spinner."""

    def progress_callback(completed: int, total: int):
        spinner.update_progress(completed, total)

    return await to_thread(fetch, sources, progress_callback)


async def _generate_adlist():
    """Generate merged adlist file from ``ADLISTS`` into ``ADLIST_OUTPUT``."""
    global status_display
    if status_display is None:
        raise RuntimeError("Status display not initialized")
    spinner = status_display.allocate_line()

    sources = await spinner.show_progress(
        "Adlist: Loading sources...", to_thread(load_sources, ADLISTS)
    )

    results: List[Tuple[str, List[str]]]
    failed_sources: List[Source]
    results, failed_sources = await spinner.show_progress(
        "Adlist: Fetching content...", _fetch_with_progress(sources, spinner)
    )

    domains, abp_rules, failed_sources = await spinner.show_progress(
        "Adlist: Processing domains...",
        to_thread(generate_list, results, sources, failed_sources),
    )

    combined = domains + abp_rules
    header = _generate_header(
        "Adlist",
        len(combined),
        len(domains),
        len(abp_rules),
        len(sources),
    )
    await spinner.show_progress(
        "Adlist: Writing output...",
        to_thread(
            write_output, ADLIST_OUTPUT, sorted(set(combined), key=str.lower), header
        ),
    )
    spinner.update_status(
        f"✅ Adlist: Complete - {len(combined)} entries ({len(domains)} domains, {len(abp_rules)} ABP rules)"
    )

    return (
        len(sources),
        len(combined),
        len(domains),
        len(abp_rules),
        failed_sources,
        results,
        sources,
    )


